// ─── Write Task File ────────────────────────────────────────────────────

export function writeTaskFile(role: Role, task: string): void {
  ensureDir();
  const path = join(PW_DIR(), `task-${role}.json`);
  const payload = { task, assignedAt: Date.now() };
  writeFileSync(path, JSON.stringify(payload, null, 2));
}

export function clearTaskFile(role: Role): void {
  const path = join(PW_DIR(), `task-${role}.json`);
  if (existsSync(path)) {
    try {
      unlinkSync(path);
    } catch {}
  }
}

// ─── Read Output File ───────────────────────────────────────────────────

export interface QaOutput {
  status: "success" | "failure" | "blocker" | "unknown";
  body: string;
  mtime: number;
}

/**
 * Parse QA output file looking for predefined tags.
 * Format: first line is tag, rest is message body.
 */
export function readQaOutput(): QaOutput | null {
  const path = join(PW_DIR(), "output-qa.txt");
  if (!existsSync(path)) return null;
  try {
    const content = readFileSync(path, "utf-8");
    const mtime = statSync(path).mtimeMs;
    const lines = content.split("\n");
    const statusLine = (lines[0] || "").trim().toUpperCase();
    const body = lines.slice(1).join("\n").trim();

    let status: QaOutput["status"] = "unknown";
    if (statusLine === "SUCCESS" || statusLine === "[SUCCESS]") {
      status = "success";
    } else if (statusLine === "FAILURE" || statusLine === "[FAILURE]") {
      status = "failure";
    } else if (statusLine === "BLOCKER" || statusLine === "[BLOCKER]") {
      status = "blocker";
    } else {
      // Also check inline in body
      const upper = content.toUpperCase();
      if (upper.includes("[SUCCESS]")) status = "success";
      else if (upper.includes("[FAILURE]")) status = "failure";
      else if (upper.includes("[BLOCKER]")) status = "blocker";
    }

    // Also check body for inline tags
    let cleanedBody = body;
    const inlineTagMatch = cleanedBody.match(/\[(SUCCESS|FAILURE|BLOCKER)\]/i);
    if (inlineTagMatch) {
      status = inlineTagMatch[1].toLowerCase() as QaOutput["status"];
      cleanedBody = cleanedBody
        .replace(/\[(SUCCESS|FAILURE|BLOCKER)\]/gi, "")
        .trim();
    }

    return { status, body: cleanedBody, mtime };
  } catch {
    return null;
  }
}

/**
 * Write the QA output file (used when injecting task context).
 */
export function writeQaOutput(status: string, body: string): void {
  ensureDir();
  const path = join(PW_DIR(), "output-qa.txt");
  const content = `${status}\n${body}`;
  writeFileSync(path, content);
}

// ─── Advance / Fallback / Escalate ──────────────────────────────────────

export function advanceStep(state: WorkflowState): WorkflowState {
  const nextIdx = state.stepIndex + 1;

  if (nextIdx >= WORKFLOW_STEPS.length) {
    state.status = "completed";
    state.role = "qa";
    writeState(state);
    return state;
  }

  const nextStep = WORKFLOW_STEPS[nextIdx];
  if (state.phase === "build" && nextStep.phase === "release") {
    state.phase = "release";
  }

  state.stepIndex = nextIdx;
  state.role = nextStep.role;
  state.roundsToAdvance = (state as any).roundsToAdvance ?? 10; // reset rounds for new step pair
  state.confidenceThreshold = state.confidenceThreshold ?? 95;
  state.qaOutputFile = join(PW_DIR(), "output-qa.txt");
  state.devTaskFile = join(PW_DIR(), "task-dev.json");
  state.qaTaskFile = join(PW_DIR(), "task-qa.json");
  writeState(state);
  return state;
}

/** Force advance past confidence check (max rounds reached). */
export function forceAdvanceStep(state: WorkflowState): WorkflowState {
  const isLastStep =
    state.stepIndex >= WORKFLOW_STEPS.length - 1 ||
    WORKFLOW_STEPS[state.stepIndex + 1] === undefined;
  if (isLastStep) return markCompleted(state);

  const scoreSum =
    (state.context.devScore ?? 0) + Math.min(100, state.context.qaScore ?? 0);
  const maxRounds = (state as any).roundsToAdvance ?? 10;
  console.log(
    `[pworkflow] max rounds reached (${maxRounds}/10), force advancing — sum ${scoreSum} < ${state.confidenceThreshold ?? 95}`,
  );
  state.context.notes = `${state.context.notes || ""}\n[Force-advanced after ${maxRounds} rounds, confidence sum ${scoreSum} < threshold ${state.confidenceThreshold ?? 95}]`;
  return advanceStep(state);
}

export function failBackToDev(
  state: WorkflowState,
  qaFeedback: string,
): WorkflowState {
  state.context.qaFeedback = qaFeedback;

  const scoreSum =
    (state.context.devScore ?? 0) + Math.min(100, state.context.qaScore ?? 0);
  const threshold = state.confidenceThreshold ?? 95;

  // Subtract rounds and check if we hit zero
  const prevToAdvance = (state as any).roundsToAdvance ?? 10;
  const newToAdvance = prevToAdvance - 1;
  if (newToAdvance <= 0) {
    // Max iterations exhausted — force push back to dev
    console.log(
      `[pworkflow] max iterations (${prevToAdvance}) reached after QA failure, pushing back to dev`,
    );
    const phaseSteps = WORKFLOW_STEPS.filter((s) => s.phase === state.phase);
    const devSteps = phaseSteps.filter((s) => s.role === "dev");
    if (devSteps.length > 0) {
      const lastDevStep = devSteps[devSteps.length - 1];
      state.stepIndex = WORKFLOW_STEPS.indexOf(lastDevStep);
    }
    state.role = "dev";
    state.status = "in_progress";
    writeState(state);
    return state;
  }

  (state as any).roundsToAdvance = newToAdvance;
  state.context.notes = `${state.context.notes || ""}\n[Round ${prevToAdvance}/${10}: confidence sum ${scoreSum} < threshold ${threshold}, QA pushed back]`;
  writeState(state);
  return state;
}

/**
 * Mark workflow as blocked (blocker path).
 */
export function markBlocked(
  state: WorkflowState,
  notes?: string,
): WorkflowState {
  state.status = "blocked";
  state.context.notes = notes || "Blocked by QA — human intervention required";
  writeState(state);
  return state;
}

/**
 * Mark workflow as failed.
 */
export function markFailed(
  state: WorkflowState,
  reason?: string,
): WorkflowState {
  state.status = "failed";
  state.context.notes = reason || "Workflow failed";
  writeState(state);
  return state;
}

/**
 * Mark workflow as completed (successful release).
 */
export function markCompleted(state: WorkflowState): WorkflowState {
  state.status = "completed";
  state.context.notes = "Release completed successfully";
  writeState(state);
  return state;
}

export function PW_DIR(): string {
  return join(process.cwd(), ".pworkflow");
}
