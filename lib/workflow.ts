() => PW_DIR();

function ensureDir(): void {
  const dir = PW_DIR();
  if (!existsSync(dir)) {
    mkdirSync(dir, { recursive: true });
  }
}

// ─── Read / Write State ─────────────────────────────────────────────────

export function readState(): WorkflowState | null {
  const path = join(PW_DIR(), "state.json");
  if (!existsSync(path)) return null;
  try {
    return JSON.parse(readFileSync(path, "utf-8"));
  } catch {
    return null;
  }
}

export function writeState(state: WorkflowState): void {
  ensureDir();
  state.updatedAt = Date.now();
  const path = join(PW_DIR(), "state.json");
  writeFileSync(path, JSON.stringify(state, null, 2));
}

// ─── Initialise / Reset ─────────────────────────────────────────────────

export function initState(): WorkflowState {
  ensureDir();

  // Create .gitignore in project root to ignore .pworkflow/
  try {
    const rootGitignore = join(process.cwd(), ".gitignore");
    if (!existsSync(rootGitignore)) {
      writeFileSync(rootGitignore, ".pworkflow/\n", "utf-8");
    } else {
      // Ensure .pworkflow/ is listed
      const existing = readFileSync(rootGitignore, "utf-8");
      if (!existing.includes(".pworkflow/")) {
        writeFileSync(rootGitignore, `.pworkflow/\n${existing}`, "utf-8");
      }
    }
  } catch {}

  // Init git repo if not already tracked
  try {
    const gitDir = join(process.cwd(), ".git");
    if (!existsSync(gitDir)) {
      console.log(`[pworkflow] Running git init in ${process.cwd()}`);
      const result = spawnSync("git", ["init"], {
        cwd: process.cwd(),
        stdio: "inherit",
      });
      if (result.status === 0 && existsSync(gitDir)) {
        // Log git init
        const logDir = join(process.cwd(), ".pworkflow");
        if (!existsSync(logDir)) mkdirSync(logDir, { recursive: true });
        const logPath = join(logDir, "git-init.log");
        appendFileSync(
          logPath,
          `${new Date().toISOString()} - git repo initialized in ${process.cwd()}\n`,
        );
      } else {
        console.warn(
          `[pworkflow] git init failed with status ${result.status} or .git not found after init`,
        );
      }
    }
  } catch (err) {
    console.warn(`[pworkflow] Could not run git init: ${err}`);
  }

  const state: WorkflowState = {
    phase: "plan",
    role: "dev",
    stepIndex: 0,
    startedAt: Date.now(),
    updatedAt: Date.now(),
    status: "in_progress",
    qaOutputFile: join(PW_DIR(), "output-qa.txt"),
    devTaskFile: join(PW_DIR(), "task-dev.json"),
    qaTaskFile: join(PW_DIR(), "task-qa.json"),
    confidenceThreshold: 95,
    roundsToAdvance: 10,
    context: {},
  };
  writeState(state);
  return state;
}

export function resetState(): void {
  // Remove all state files for clean restart
  const dir = PW_DIR();
  if (!existsSync(dir)) return;
  try {
    const files = [
      "state.json",
      "task-dev.json",
      "task-qa.json",
      "output-qa.txt",
    ];
    for (const f of files) {
      try {
        try {
          rmSync(join(dir, f));
        } catch {
          try {
            unlinkSync(join(dir, f));
          } catch {}
        }
      } catch {}
    }
  } catch {}
}

